	<h2>Alux Nahual</h2>
	<div id="informacion">
	<p id="parrafo1">Alux Nahual es una banda de rock guatemalteca que se fundó el 16 de junio de 1979. Desde 1981 hasta 1990 publicó 8 producciones de estudio bajo el sello discográfico Discos de Centroamérica (DIDECA), con los que se dio a conocer en toda la región: Alux Nahual, Conquista, Hermanos de Sentimiento, Centroamérica, Alto al Fuego (certificado Disco de Oro por ventas), La Trampa, Con Tus Manos (EP) y Leyenda (Certificado Disco de Platino por ventas). Su éxito los llevó a firmar con Sony Music, sello con el que grabaron dos producciones más: Americamorfosis (Certificado Disco de Oro) en 1993 y Se Cantan Retratos en 1996. La banda se convirtió en el grupo más exitoso de la región, teniendo más de 23 sencillos radiales y llenando localidades en Guatemala, Honduras, El Salvador, Costa Rica y Estados Unidos. Se desintegró en 1999 aunque los músicos siguieron colaborando en diferentes producciones como solistas. A partir de 2005, la banda se reintegró para presentarse ocasionalmente aunque no crearon nuevo material. Luego de muchos años de planificación, Alux Nahual interpretó sus mejores éxitos junto a una orquesta sinfónica en 2010, concierto que fue publicado en DVD en 2011 bajo el nombre de Alux Nahual Sinfónico. En 2012, presentaron su primer disco de estudio en 16 años: Murciélago Danzante, el cual se presentó en la Ermita de La Santa Cruz en La Antigua Guatemala el 8 de diciembre de ese año. El 10 de febrero de 2014, se lanzó a radios su primer sencillo radial en más de una década, Te Abrazaré versión alternativa, con la que volvieron a entrar dentro de la programación masiva en el país. Actualmente se celebran sus 35 años de carrera musical.
<br>
<br>
Alux Nahual está integrado actualmente por los miembros originales: Álvaro Aguilar (voz, guitarra acústica), Ranferí Aguilar (voz y guitarras eléctricas y acústicas), Paulo Alvarado (Chelo y teclados) y Plubio Aguilar (voz y bajo). También integran la banda Óscar Conde (flauta, saxofón y teclados), quien fue invitado desde 1982 y Vinicio Molina (batería), que comenzó su colaboración con la banda previo a grabar el disco Murciélago Danzante en 2012.</p>
